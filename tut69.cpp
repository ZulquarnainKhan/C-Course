// -------------------- INTRODUCTION TO STL -------------------------
/*
STL- Standard Template Library
    STL is a library of generic classes and functions 
    Very useful in competative programming
    Used for : resizing , searching, sorting etc...

Why use STL:
    -Reusability: We can use well tested classes and functions using STL
    -Time saving
    -It is a good idea not to reinvent the wheel 

Components of STL:
    1. Containers: Stores data
    2. Algorithms: sorting ,searching and many other works done by algorithms using template fuctions
    3. Iterations: this object points to an element in container , handeled just like pointers , connects algorithms to containers

*/